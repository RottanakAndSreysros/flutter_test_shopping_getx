import 'package:flutter/material.dart';

Widget textSize24(
  String text, {
  var color = Colors.black,
  FontWeight fontWeight = FontWeight.w300,
  TextOverflow textOverflow = TextOverflow.visible,
  int maxLines = 1,
}) {
  return Text(
    text,
    style: TextStyle(
      fontSize: 24,
      fontWeight: fontWeight,
      color: color,
    ),
    overflow: textOverflow,
    maxLines: maxLines,
  );
}

